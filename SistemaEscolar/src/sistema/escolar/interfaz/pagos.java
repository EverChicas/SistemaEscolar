    private void cargarPagos(){
        List < String > listaDePagos;
        Conexion con = new Conexion();
        listaDePagos = con.cargarDatos("pago", "nombre");
        
        for(int i = 0; i < listaDePagos.size(); i++){
            cmbNombre.addItem(listaDePagos.get(i));
        }
    }
    

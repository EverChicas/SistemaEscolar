    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        if(cmbNombre.getSelectedItem() != null) { 
            Conexion con = new Conexion();
            alumno = con.buscarAlumno(cmbNombre.getSelectedItem().toString());
            txtCarnet.setText(alumno.getCarnet() + "");
            txtCarnet.setEditable(false);
            txtNombre.setText(alumno.getNombre());
            txtApellido.setText(alumno.getApellido());
            txtEdad.setText(alumno.getEdad() + "");
            txtTelefono.setText(alumno.getTelefono());
            txtGrado.setText(alumno.getGrado().getNombre());
            txtIdTutor.setText(alumno.getTutor().getIdTutor() + "");
            txtNombreTutor.setText(alumno.getTutor().getNombre());
            txtApellidoTutor.setText(alumno.getTutor().getApellido());
            txtTelefonoTutor.setText(alumno.getTutor().getTelefono());
            grado = alumno.getGrado();
            tutor = alumno.getTutor();
        }
    }//GEN-LAST:event_btnBuscarActionPerformed


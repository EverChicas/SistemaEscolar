    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if(alumno != null && grado != null && tutor != null){
            Conexion con = new Conexion();
            alumno.setCarnet(Integer.parseInt(txtCarnet.getText()));
            alumno.setNombre(txtNombre.getText());
            alumno.setApellido(txtApellido.getText());
            alumno.setEdad(Integer.parseInt(txtEdad.getText()));
            alumno.setTelefono(txtTelefono.getText());
            alumno.setGrado(grado);
            alumno.setTutor(tutor);
            if(String.valueOf(alumno.getCarnet()).equalsIgnoreCase(txtCarnet.getText())){
                con.actualizarAlumno(alumno);
            }else{
                con.insertarAlumno(alumno);
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed


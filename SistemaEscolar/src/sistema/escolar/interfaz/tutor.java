    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        tutor = null;
        tutor.setIdTutor(Integer.parseInt(txtIdTutor.getText()));
        tutor.setNombre(txtNombreTutor.getText());
        tutor.setApellido(txtApellidoTutor.getText());
        tutor.setTelefono(txtTelefonoTutor.getText());
        Conexion con = new Conexion();
        con.insertarTutor(tutor);
    }//GEN-LAST:event_btnGuardarActionPerformed


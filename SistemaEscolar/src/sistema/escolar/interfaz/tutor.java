    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        if(!"".equals(cmbNombreTutor.getSelectedItem().toString())){
            Conexion con = new Conexion();
            tutor = con.buscarTutor(cmbNombreTutor.getSelectedItem().toString());
            txtIdTutor.setText(tutor.getIdTutor() + "");
            txtNombreTutor.setText(tutor.getNombre());
            txtApellidoTutor.setText(tutor.getApellido());
            txtTelefonoTutor.setText(tutor.getTelefono());
        }
    }//GEN-LAST:event_btnBuscarActionPerformed


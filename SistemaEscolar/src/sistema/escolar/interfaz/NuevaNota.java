    private void mostrarCurso(){
        List < String > listaDeCursos;
        Conexion con = new Conexion();
        listaDeCursos = con.cargarDatos("curso", "nombre");
        
        for(int i = 0; i < listaDeCursos.size(); i++){
            cmbCurso.addItem(listaDeCursos.get(i));
        }
    }
    

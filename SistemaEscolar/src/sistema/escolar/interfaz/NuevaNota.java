    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if(!"".equals(txtNota.getText())){
            JOptionPane.showMessageDialog(null,"Ingresar una nota");
        }else{
            Conexion con = new Conexion();
            Alumno alumno = con.buscarAlumno(cmbAlumno.getSelectedItem().toString());
            Curso curso = con.buscarCurso(cmbCurso.getSelectedItem().toString());
            NotasAlumno nueva = new NotasAlumno(alumno,curso,Integer.parseInt(txtNota.getText()));
            
            if(con.existeNota(alumno, curso)){
                con.actualizarNotaDeAlumno(nueva);
            }else{
               con.insertarNotaDeAlumno(nueva); 
            }           
        }
    }//GEN-LAST:event_btnGuardarActionPerformed


    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        pago = null;
        cmbNombre.setVisible(false);
        pago.setIdPago(Integer.parseInt(txtIdPago.getText()));
        pago.setNombre(txtNombre.getText());
        pago.setCantidad(Double.parseDouble(txtCantidad.getText()));
        Conexion con = new Conexion();
        con.insertarPago(pago);
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        if(!"".equals(cmbNombre.getSelectedItem().toString())){
            Conexion con = new Conexion();
            pago = con.buscarPago(cmbNombre.getSelectedItem().toString());
            txtIdPago.setText(pago.getIdPago()+ "");
            txtIdPago.setEnabled(false);
            txtNombre.setText(pago.getNombre());
            txtCantidad.setText(pago.getCantidad() + "");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

